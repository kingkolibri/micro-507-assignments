function A_p = eval_A_p(q_p)
    [m, ~, ~, l1, l2, l3, ~] = set_param();
    
    q1_p = q_p(1);
    q2_p = q_p(2);
    q3_p = q_p(3);
    
    t2 = l1.^2;
    t3 = q1_p.*t2.*8.0;
    t4 = q1_p+q2_p;
    t5 = cos(t4);
    t6 = q1_p+q3_p;
    t7 = cos(t6);
    t8 = l1.*l3.*q1_p.*t7.*2.0;
    t11 = l1.*l2.*q1_p.*t5.*2.0;
    t9 = t3+t8-t11;
    t10 = m.*t9.*(1.0./4.0);
    t12 = q1_p.*t2.*4.0;
    t13 = t8+t12;
    t14 = m.*t13.*(1.0./2.0);
    t15 = l3.^2;
    t16 = l1.*l3.*t7.*2.0;
    t17 = t15+t16;
    A_p = reshape([ t10+m.*t2.*(1.0./4.0),...
                    m.*t2.*(1.0./2.0),...
                    t14,...
                    t10-m.*(l2.^2-l1.*l2.*t5.*2.0).*(1.0./4.0),...
                    0.0,...
                    t14,...
                    t10-m.*t17.*(1.0./4.0),...
                    0.0,...
                    t14-m.*t17.*(1.0./2.0)...
                 ],...
                 [3,3]);
end